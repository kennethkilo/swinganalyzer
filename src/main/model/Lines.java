package model;

public class Lines {
    static int lineWidth;
    static String lineColour;
    static String lineStart;
    static String lineEnd;

    public Lines(String lineColour, int lineWidth, String lineStart, String lineEnd) {
        this.lineColour = lineColour;
        this.lineWidth = lineWidth;
        this.lineStart = lineStart;
        this.lineEnd = lineEnd;

    }


}
